namespace PaperWalls.Tests.Helpers;

internal static class ValidImageBytes
{
    // 4-byte minimal PNG header — sufficient for magic-byte validation
    public static readonly byte[] PngShort = [0x89, 0x50, 0x4E, 0x47];

    // Full PNG signature (8 bytes) + IHDR length field
    public static readonly byte[] Png = [0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D];

    // JFIF JPEG header
    public static readonly byte[] Jpeg = [0xFF, 0xD8, 0xFF, 0xE0, 0x00, 0x10, 0x4A, 0x46, 0x49, 0x46, 0x00, 0x01];

    // BMP file header ("BM" + file size + reserved bytes + pixel array offset)
    public static readonly byte[] Bmp = [0x42, 0x4D, 0x46, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x36, 0x00];

    // WEBP container: "RIFF" + 4-byte file size + "WEBP"
    public static readonly byte[] Webp = [0x52, 0x49, 0x46, 0x46, 0x24, 0x00, 0x00, 0x00, 0x57, 0x45, 0x42, 0x50];
}
